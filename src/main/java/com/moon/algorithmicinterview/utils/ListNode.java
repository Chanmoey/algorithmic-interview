package com.moon.algorithmicinterview.utils;

/**
 * @author Chanmoey
 * @date 2023年07月09日
 */

public class ListNode {
    public int val;
    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}

